function renderGroupFocus() {
  const target = document.querySelector("#groupFocus");
  if (!target) return;

  const group = getSelectedGroup();
  if (!group) {
    target.hidden = true;
    target.innerHTML = "";
    return;
  }

  const groupEvents = state.events
    .filter((event) => getEventGroup(event) === group)
    .sort((a, b) => new Date(a.date) - new Date(b.date));

  const schedule = groupEvents.length
    ? groupEvents.map((event) => {
      const competition = event.competitions?.[0] || {};
      const teams = getEventTeams(event);
      const home = teamNameJa(teams[0] || "未定");
      const away = teamNameJa(teams[1] || "未定");
      const venue = competition.venue?.fullName || "";

      return `<article class="group-match-card">
        <div>
          <time datetime="${event.date}">${formatTokyoDateLabel(new Date(event.date), "Asia/Tokyo")} ${formatTokyoTime(new Date(event.date))}</time>
          <strong>${home}<span>vs</span>${away}</strong>
          <small>${venue}</small>
        </div>
        <span class="match-status">${getEventStatusText(event)} / ${getEventScoreText(event)}</span>
      </article>`;
    }).join("")
    : `<p class="empty-state">このグループの試合予定はまだ取得できません。</p>`;

  target.hidden = false;
  target.innerHTML = `<div class="section-heading">
    <p class="eyebrow">グループ${group}</p>
    <h2>順位表と試合予定</h2>
  </div>
  <div class="group-focus-grid">
    <article class="group-focus-panel">
      <h3>順位表</h3>
      ${groupTableHtml(group)}
    </article>
    <article class="group-focus-panel">
      <h3>試合予定</h3>
      <div class="group-match-list">${schedule}</div>
    </article>
  </div>`;
}

