function startAutoRefresh() {
  if (refreshTimer) return;
  refreshTimer = window.setInterval(loadData, 60_000);
}

function stopAutoRefresh() {
  if (!refreshTimer) return;
  window.clearInterval(refreshTimer);
  refreshTimer = null;
}

document.addEventListener("visibilitychange", () => {
  if (document.hidden) {
    stopAutoRefresh();
    return;
  }

  loadData();
  startAutoRefresh();
});

startAutoRefresh();
