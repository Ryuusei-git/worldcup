function renderBracket() {
  const round32 = document.querySelector("#round32");
  const qualified = document.querySelector("#qualifiedBracket");
  if (!round32 || !qualified) return;

  const { thirds } = getQualified();
  const thirdAssignments = getCurrentThirdAssignments(thirds);

  round32.innerHTML = ROUND32_MATCHES.map((match, index) => {
    const body = match.slots.map((slot) => {
      return `<div class="bracket-team ${slot.type}">
        <span>${seedLabel(slot)}</span>
        <strong>${seedDisplayName(slot, state.standings, thirdAssignments, match.match)}</strong>
      </div>`;
    }).join("");

    return matchCardHtml(match.match, index * BRACKET_MATCH_ROW_SPAN + 1, body);
  }).join("");

  FUTURE_ROUNDS.forEach((round) => {
    const target = document.querySelector(`#${round.id}`);
    if (!target) return;

    const rowGap = BRACKET_GRID_ROWS / round.matches.length;
    target.innerHTML = round.matches.map((match, index) => {
      const sourceLabel = match.sourceLabel || "勝者";
      const body = match.sources.map((source) => (
        `<div class="bracket-team">
          <span>${sourceLabel}</span>
          <strong>Match ${source}</strong>
        </div>`
      )).join("");

      return matchCardHtml(match.match, Math.floor(index * rowGap + rowGap / 2), body);
    }).join("");
  });

  qualified.innerHTML = getQualified().all.map((team) => (
    `<article class="qualified-card ${team.status.includes("3位") ? "third" : "direct"}">
      <small>グループ${team.group} / ${team.status}</small>
      <strong>${teamNameJa(team.name)}</strong>
    </article>`
  )).join("");
}

function render() {
  renderStatus();

  if (page === "home") {
    renderTomorrowSchedule();
    renderQualifiedPreview();
    renderThirdPreview();
    return;
  }

  if (page === "standings") {
    renderGroupFocus();
    renderStandings();
    renderThirdTable();
    return;
  }

  if (page === "bracket") {
    renderBracket();
  }
}

